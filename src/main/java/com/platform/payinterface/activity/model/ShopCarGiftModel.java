package com.platform.payinterface.activity.model;

/**
 * 购物车赠品模型
 * @author 李卫海
 *
 */
public class ShopCarGiftModel {

	private Integer id;
	private String name;
	private String pc_default_image;
	private String stylename;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPc_default_image() {
		return pc_default_image;
	}
	public void setPc_default_image(String pc_default_image) {
		this.pc_default_image = pc_default_image;
	}
	public String getStylename() {
		return stylename;
	}
	public void setStylename(String stylename) {
		this.stylename = stylename;
	}
	

}
